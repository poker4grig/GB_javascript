let basket = {
    'monitor': {
        'name': 'monitor',
        'count': 5,
        'price': 5000
    },
    'gamepad': {
        'name': 'gamepad',
        'count': 50,
        'price': 1000
    },
    'video card': {
        'name': 'video card',
        'count': 10,
        'price': 10000
    },
}
let totalPrice = 0;
for (let key in basket) {
    totalPrice += basket[key].count * basket[key].price
}

alert(`Итоговая сумма товаров, содержащихся в объекте "корзина"
равна ${totalPrice} рублей.`);
