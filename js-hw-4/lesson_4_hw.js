/*1. 1. Написать функцию, преобразующую число в объект. Передавая на вход число от 0 до 999, мы должны получить на выходе объект, в котором в соответствующих свойствах описаны единицы, десятки и сотни. Например, для числа 245 мы должны получить следующий объект: {‘единицы’: 5, ‘десятки’: 4, ‘сотни’: 2}. Если число превышает 999, необходимо выдать соответствующее сообщение с помощью console.log и вернуть пустой объект.*/
let num = parseInt(prompt('Введите число от 0 до 999'));
let numObj = {
    'единицы': null,
    'десятки': null,
    'сотни': null
};

function fillNumObj(num) {
    numObj.сотни = Math.floor(num / 100);
    numObj.десятки = Math.floor(num / 10) % 10;
    numObj.единицы = num % 10;
}

fillNumObj(num);

for (let key in numObj) {
    alert(`${key}: ${numObj[key]}`);
}

